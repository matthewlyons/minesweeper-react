export default function useColor(num) {
  switch (num) {
    case 1:
      return 'Blue';
    case 2:
      return 'Green';
    case 3:
      return 'Red';
    case 4:
      return 'Purple';
    case 5:
      return 'Maroon';
    case 6:
      return 'Turquoise';
    case 7:
      return 'Black';
    case 8:
      return 'Gray';
    default:
      return 'Black';
  }
}
